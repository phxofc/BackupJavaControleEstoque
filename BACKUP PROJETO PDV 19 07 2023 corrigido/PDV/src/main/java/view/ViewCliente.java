    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed

        if (salvarEalterar.equals("salvar")) {
            this.salvarCliente();
        } else if (salvarEalterar.equals("alterar")) {
            this.alterarCliente();
        }

    }//GEN-LAST:event_jbSalvarActionPerformed

    private void carregarCliente() {
        listaModelClientes = controllerClientes.getAllClienteController();
        DefaultTableModel modelo = (DefaultTableModel) jtCliente.getModel();
        modelo.setNumRows(0);
        //pegar tamanho da lista pra pecorrer
        int cont = listaModelClientes.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelClientes.get(i).getIdCliente(),
                listaModelClientes.get(i).getCliNome(),
                listaModelClientes.get(i).getCliCidade(),
                listaModelClientes.get(i).getCliTelefone()
            });
        }
    }


    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        salvarEalterar = "alterar";
        habilitarDesabilitar(true);
        int linha = jtCliente.getSelectedRow();
        try {
            int codigoCliente = (int) jtCliente.getValueAt(linha, 0);

            //pegarprodut
            modelCliente = controllerClientes.getClienteController(codigoCliente);
            //setar na interface
            this.jtfCodigo.setText(String.valueOf(codigoCliente));
            this.jtfNome.setText(modelCliente.getCliNome());
            this.jtfEndereco.setText((modelCliente.getCliEndereco()));
            this.jtfBairro.setText((modelCliente.getCliBairro()));
            this.jtfCidade.setText((modelCliente.getCliCidade()));
            this.jcbUF.setSelectedItem((modelCliente.getCliUf()));
            this.jtfCep.setText((modelCliente.getCliCep()));
            this.jtfTelefone.setText((modelCliente.getCliTelefone()));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código errado ou nenhum registro selecionado", "Aviso", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jbAlterarActionPerformed

    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        int linha = jtCliente.getSelectedRow();
        int codigoCliente = (int) jtCliente.getValueAt(linha, 0);

        if (controllerClientes.DeletarClienteController(codigoCliente)) {
            JOptionPane.showMessageDialog(rootPane, "EXCLUIDO COM SUCESSO");
            this.carregarCliente();
        } else {
            JOptionPane.showMessageDialog(rootPane, "erro ao excluir cliente");
        }


    }//GEN-LAST:event_jbExcluirActionPerformed

