    public void salvarCliente() {

        //VERIFICAR SE VAI ALTERAR OU SALVAR
        
        modelCliente.setCliNome(this.jtfNome.getText());
        modelCliente.setCliEndereco(this.jtfEndereco.getText());
        modelCliente.setCliBairro(this.jtfBairro.getText());
        modelCliente.setCliCidade(this.jtfCidade.getText());
        modelCliente.setCliUf(this.jcbUF.getSelectedItem().toString());
        modelCliente.setCliCep(this.jtfCep.getText());
        modelCliente.setCliTelefone(this.jtfTelefone.getText());

        if (controllerClientes.SalvarClienteController(modelCliente)) {
            JOptionPane.showMessageDialog(this, "CLIENTE SALVO COM SUCESSO", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
            carregarCliente();
            this.LimparCampos();
            this.habilitarDesabilitar(false);
            //CARREGAR OS CLIENTES NA TABELA
        } else {
            JOptionPane.showMessageDialog(this, "ERRO AO SALVO CLIENTE", "ERRO!!!", JOptionPane.ERROR_MESSAGE);
        }

    }

    public void alterarCliente() {
        modelCliente.setIdCliente(Integer.parseInt(this.jtfCodigo.getText()));
        modelCliente.setCliNome(this.jtfNome.getText());
        modelCliente.setCliEndereco(this.jtfEndereco.getText());
        modelCliente.setCliBairro(this.jtfBairro.getText());
        modelCliente.setCliCidade(this.jtfCidade.getText());
        modelCliente.setCliUf(this.jcbUF.getSelectedItem().toString());
        modelCliente.setCliCep(this.jtfCep.getText());
        modelCliente.setCliTelefone(this.jtfTelefone.getText());

        if (controllerClientes.AlterarClienteController(modelCliente)) {
            JOptionPane.showMessageDialog(this, "CLIENTE ALTERADO COM SUCESSO", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
            carregarCliente();
            this.LimparCampos();
            this.habilitarDesabilitar(false);
            //CARREGAR OS CLIENTES NA TABELA
        } else {
            JOptionPane.showMessageDialog(this, "ERRO AO ALTERAR CLIENTE", "ERRO!!!", JOptionPane.ERROR_MESSAGE);
        }
    }

