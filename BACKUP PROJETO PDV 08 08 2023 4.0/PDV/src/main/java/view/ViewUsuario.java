    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed

        try {
            modelUsuario.setIdUsuario(Integer.parseInt(jtfCodigo.getText()));
        } catch (NumberFormatException e) {
        }

        modelUsuario.setUsuNome(jtfNome.getText());

        modelUsuario.setUsuLogin(jtfLogin.getText());
        modelUsuario.setUsuSenha(String.valueOf(this.jpSenha.getPassword()));

        if (salvarEalterar.equals("salvar")) {
            if (controllerUsuarios.SalvarUsuarioController(modelUsuario)) {
                JOptionPane.showMessageDialog(rootPane, "SALVO COM SUCESSO");
                carregarUsuario();
                LimparCampos();
                habilitarDesabilitar(false);
            } else {
                JOptionPane.showMessageDialog(rootPane, "ERRO AO SALVAR");
            }
        }else{
            if (controllerUsuarios.AlterarUsuarioController(modelUsuario)) {
                JOptionPane.showMessageDialog(rootPane, "ALTERADO COM SUCESSO");
                carregarUsuario();
                LimparCampos();
                habilitarDesabilitar(false);
            } else {
                JOptionPane.showMessageDialog(rootPane, "ERRO AO ALTERAR");
            }
        }


    }//GEN-LAST:event_jbSalvarActionPerformed

    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        salvarEalterar = "alterar";
        habilitarDesabilitar(true);

        int linha = jtUsuario.getSelectedRow();

        try {
            int codigoUsuario = (int) jtUsuario.getValueAt(linha, 0);
            //pegarUsuario
            modelUsuario = controllerUsuarios.getUsuarioController(codigoUsuario);
            this.jtfCodigo.setText(String.valueOf(modelUsuario.getIdUsuario()));
            this.jtfNome.setText(modelUsuario.getUsuNome());
            this.jtfLogin.setText(modelUsuario.getUsuLogin());
            this.jpSenha.setText(modelUsuario.getUsuSenha());

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código errado ou nenhum registro selecionado", "Aviso", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_jbAlterarActionPerformed

    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        int linha = jtUsuario.getSelectedRow();
        int codigoUsuario = (int) jtUsuario.getValueAt(linha, 0);

        if (controllerUsuarios.DeletarUsuariosController(codigoUsuario)) {
            JOptionPane.showMessageDialog(rootPane, "EXCLUIDO COM SUCESSO");
            this.carregarUsuario();
        } else {
            JOptionPane.showMessageDialog(rootPane, "erro ao excluir usuario");
        }

    }//GEN-LAST:event_jbExcluirActionPerformed

