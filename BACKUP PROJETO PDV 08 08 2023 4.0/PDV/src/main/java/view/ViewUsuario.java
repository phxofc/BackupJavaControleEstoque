    //carregar usuarios do banco
    public void carregarUsuario() {
        listaModelUsuarios = controllerUsuarios.getAllUsuarioController();
        DefaultTableModel modelo = (DefaultTableModel) jtUsuario.getModel();
        modelo.setRowCount(0);
        //pegar tamanho da lista para pecorrer
        int cont = listaModelUsuarios.size();

        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelUsuarios.get(i).getIdUsuario(),
                listaModelUsuarios.get(i).getUsuLogin(),
                listaModelUsuarios.get(i).getUsuNome()
            });
        }
    }

    public void alterarUsuario() {
        modelUsuario.setIdUsuario(Integer.parseInt(this.jtfCodigo.getText()));
        modelUsuario.setUsuNome(this.jtfNome.getText());
        modelUsuario.setUsuLogin(this.jtfLogin.getText());
        modelUsuario.setUsuSenha(String.valueOf(this.jpSenha.getPassword()));

        if (controllerUsuarios.AlterarUsuarioController(modelUsuario)) {
            JOptionPane.showMessageDialog(this, "USUARIO ALTERADO COM SUCESSO", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
            carregarUsuario();
            this.LimparCampos();
            this.habilitarDesabilitar(false);
            //CARREGAR OS usuarios NA TABELA
        } else {
            JOptionPane.showMessageDialog(this, "ERRO AO ALTERAR USUARIO", "ERRO!!!", JOptionPane.ERROR_MESSAGE);
        }
    }

    public void salvarUsuario() {

        //VERIFICAR SE VAI ALTERAR OU SALVAR
        modelUsuario.setUsuNome(this.jtfNome.getText());
        modelUsuario.setUsuLogin(this.jtfLogin.getText());
        modelUsuario.setUsuSenha(String.valueOf(this.jpSenha.getPassword()));

        if (controllerUsuarios.SalvarUsuarioController(modelUsuario)) {
            JOptionPane.showMessageDialog(this, "Usuario SALVO COM SUCESSO", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
            this.carregarUsuario();
            this.LimparCampos();
            this.habilitarDesabilitar(false);
            //CARREGAR OS USUARIOS NA TABELA
        } else {
            JOptionPane.showMessageDialog(this, "ERRO AO SALVO usuario", "ERRO!!!", JOptionPane.ERROR_MESSAGE);
        }

    }


